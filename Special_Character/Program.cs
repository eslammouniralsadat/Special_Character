namespace HelloWorld
{
    class Program
    {
        static void Main(string[] args)
        {
            // Special Characters in Strings For C#

            Console.WriteLine("My Name is \' Eslam Mounir AL Sadat"); // => Single Quote => \'
            Console.WriteLine("My Name is \" Eslam Mounir AL Sadat"); // => Double Quote => \"
            Console.WriteLine("My Name is \\ Eslam Mounir AL Sadat"); // => Backslash => \\
            Console.WriteLine("My Name is \r Eslam Mounir AL Sadat"); // => Carriage Return => \r
            Console.WriteLine("My Name is \t Eslam Mounir AL Sadat"); // => Tab => \t
            Console.WriteLine("My Name is \b \b Eslam Mounir AL Sadat"); // => Backspace => \b
            Console.WriteLine("My Name is \f Eslam Mounir AL Sadat"); // => Form Feed => \f
            Console.WriteLine("My Name is \n Eslam Mounir AL Sadat"); // => New Line => \n
        }
    }
}